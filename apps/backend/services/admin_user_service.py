async def list_users(
    session: AsyncSession,
    *,
    actor: CurrentUser,  # noqa: ARG001 — kept for symmetry with other admin services
    page: int = 1,
    page_size: int = 50,
    role: str | None = None,
    active: bool | None = None,
    search: str | None = None,
) -> AdminUserListPage:
    """
    Return a page of users (paginated, filterable).

    - ``role`` filters by highest-effective role (super_admin = is_superuser;
      team_admin / developer = exists membership with that role; the filter
      is a coarse "user has at least one membership of this kind"). When
      role is ``super_admin`` we filter on ``is_superuser=true`` directly.
    - ``active`` filters by ``is_active`` exactly.
    - ``search`` is bounded substring ILIKE on email or full_name (max 255
      chars to bound the query cost).
    """
    page = max(page, 1)
    page_size = max(min(page_size, 200), 1)

    base = select(User)
    count_base = select(func.count()).select_from(User)

    if active is not None:
        base = base.where(User.is_active.is_(active))
        count_base = count_base.where(User.is_active.is_(active))

    if role == "super_admin":
        base = base.where(User.is_superuser.is_(True))
        count_base = count_base.where(User.is_superuser.is_(True))
    elif role in ("team_admin", "developer"):
        sub = select(Membership.user_id).where(Membership.role == role).scalar_subquery()
        base = base.where(User.id.in_(sub))
        count_base = count_base.where(User.id.in_(sub))

    if search:
        # Bounded substring search; the inputs are already capped at 255 in
        # the route layer (Query(max_length=255)). Search is intentionally
        # case-insensitive on email (CITEXT-friendly) and full_name.
        like = f"%{search.strip()}%"
        base = base.where(or_(User.email.ilike(like), User.full_name.ilike(like)))
        count_base = count_base.where(or_(User.email.ilike(like), User.full_name.ilike(like)))

    total = int((await session.execute(count_base)).scalar_one())

    rows_stmt = (
        base.order_by(User.created_at.desc(), User.id.desc())
        .limit(page_size)
        .offset((page - 1) * page_size)
    )
    rows = list((await session.execute(rows_stmt)).scalars().all())

    return AdminUserListPage(
        items=[AdminUserListItem.model_validate(u) for u in rows],
        total=total,
        page=page,
        page_size=page_size,
    )


