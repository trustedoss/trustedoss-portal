"""
Admin user-management service — Phase 4 PR #13.

Pure DB I/O for the ``/v1/admin/users/*`` HTTP surface. The router is a thin
adapter that translates HTTP shapes into these calls and turns the domain
exceptions below into RFC 7807 responses.

Safety contracts (CLAUDE.md "조직/팀/권한 모델" + PR #13 spec §4.2):
  - Demoting / deactivating the *last* active super_admin is rejected.
    422 with extension ``last_super_admin_protected = true``.
  - The actor cannot mutate their own role / activation state — there is
    no recovery path if the only super_admin locks themselves out.
    422 with extension ``cannot_modify_self = true``.
  - Password-reset tokens are bcrypt-hashed; the plaintext is generated by
    the issuer endpoint and intentionally discarded after issuance. Phase 6
    PR #18 wires the email channel that delivers the plaintext.
  - Single-pending-token policy: issuing a new reset token marks any
    previous unused / non-expired / non-invalidated rows for the same user
    as ``invalidated_at = now()``.

Audit hand-off:
  Mutating ORM rows is enough — the SQLAlchemy ``before_flush`` listener
  in ``core.audit`` produces the audit_logs row automatically. Sensitive
  columns (``hashed_password``, ``token_hash``) are auto-masked in the diff.
  Service code therefore must never call ``session.add(AuditLog(...))``.
"""

from __future__ import annotations

import secrets
import uuid
from datetime import UTC, datetime, timedelta

import structlog
from sqlalchemy import func, or_, select
from sqlalchemy.ext.asyncio import AsyncSession
from sqlalchemy.orm import selectinload

from core.security import CurrentUser, hash_password
from models import Membership, PasswordResetToken, RefreshToken, Scan, Team, User
from schemas.admin import (
    AdminUserDetail,
    AdminUserListItem,
    AdminUserListPage,
    AdminUserRoleUpdate,
    TeamMembershipPublic,
)

log = structlog.get_logger("admin.user.service")


# ---------------------------------------------------------------------------
# Domain exceptions
# ---------------------------------------------------------------------------


class AdminUserError(Exception):
    """Base class for admin-user errors. Each carries an HTTP status."""

    status_code: int = 400
    title: str = "Admin User Error"
    extensions: dict[str, object] = {}


class AdminUserNotFound(AdminUserError):
    status_code = 404
    title = "User Not Found"


class LastSuperAdminProtected(AdminUserError):
    status_code = 422
    title = "Last Super Admin Protected"
    extensions = {"last_super_admin_protected": True}


class CannotModifySelf(AdminUserError):
    status_code = 422
    title = "Cannot Modify Self"
    extensions = {"cannot_modify_self": True}


class InvalidRoleAssignment(AdminUserError):
    status_code = 422
    title = "Invalid Role Assignment"


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _now() -> datetime:
    return datetime.now(tz=UTC)


async def _lock_and_count_active_super_admins(session: AsyncSession) -> int:
    """
    Acquire a row-level lock on the active-super-admin set and return the count.

    Closes the TOCTOU race between SELECT-then-mutate that a plain
    ``SELECT count()`` would expose: two concurrent demote/deactivate
    transactions could each read ``count == 2``, both pass the ``> 1`` guard,
    and then both commit, dropping the active super_admin count to zero.

    The locking SELECT pulls every (is_active, is_superuser) row with
    ``FOR UPDATE``. Postgres holds those row locks until the calling
    transaction commits or rolls back — so a second concurrent call against
    any of the same rows blocks until the first finishes, at which point
    the second sees the post-commit count and the guard fires correctly.

    Lock order convention (also documented in admin_team_service):
      ``users (is_superuser=true AND is_active=true)`` is always locked
    BEFORE any ``memberships`` rows in the same transaction, to keep a
    consistent global lock order across the two admin services and avoid
    deadlocks on cross-service operations.

    Reference: feedback_optimistic_concurrency_pattern memory; pattern
    matches ``vulnerability_service.py:309`` (``with_for_update`` on the
    finding row before the optimistic-concurrency compare-and-swap).
    """
    # Pull the rows with FOR UPDATE so Postgres takes the row locks. We
    # then count from the materialized list — using a separate
    # ``SELECT count()`` would emit a second statement that does NOT take
    # the same locks. ``scalars().all()`` consumes the result and forces
    # the lock to be acquired.
    locked = (
        (
            await session.execute(
                select(User)
                .where(User.is_active.is_(True), User.is_superuser.is_(True))
                .with_for_update()
            )
        )
        .scalars()
        .all()
    )
    return len(locked)


async def _load_user_with_memberships(session: AsyncSession, user_id: uuid.UUID) -> User:
    """
    Fetch the user with its memberships eagerly loaded.

    If the User is already in the session's identity map (likely after a
    prior mutation in this same call chain), we refresh the memberships
    collection explicitly so the response sees the post-commit state.
    Without this step a User that was loaded earlier — e.g. in
    update_user_role before we added a Membership row — would be served
    from the identity map with a stale empty memberships collection.
    """
    stmt = select(User).where(User.id == user_id).options(selectinload(User.memberships))
    user = (await session.execute(stmt)).scalar_one_or_none()
    if user is None:
        raise AdminUserNotFound(f"user {user_id} not found")
    # Even though the SELECT requested memberships eagerly, SQLAlchemy may
    # serve the User from the identity map; an explicit refresh of the
    # collection guarantees we see fresh rows from any prior commit in
    # this session.
    await session.refresh(user, attribute_names=["memberships"])
    return user


async def _team_name_map(session: AsyncSession, team_ids: list[uuid.UUID]) -> dict[uuid.UUID, str]:
    if not team_ids:
        return {}
    stmt = select(Team.id, Team.name).where(Team.id.in_(team_ids))
    rows = (await session.execute(stmt)).all()
    return {row[0]: row[1] for row in rows}


async def _scan_count_for_user(session: AsyncSession, user_id: uuid.UUID) -> int:
    stmt = select(func.count()).select_from(Scan).where(Scan.requested_by_user_id == user_id)
    return int((await session.execute(stmt)).scalar_one())


