# Update role
# ---------------------------------------------------------------------------


async def update_user_role(
    session: AsyncSession,
    *,
    actor: CurrentUser,
    user_id: uuid.UUID,
    payload: AdminUserRoleUpdate,
) -> AdminUserDetail:
    """
    Set the user's role.

    Semantics:
      - ``role == "super_admin"`` -> ``user.is_superuser = True``. Existing
        memberships are not touched (the super_admin role is global; team
        memberships are independent).
      - ``role in {"team_admin", "developer"}`` -> ``user.is_superuser = False``
        AND upsert ``Membership(user_id, team_id, role)``. ``team_id`` is
        required.

    Safety: rejects (422) when the change would demote the last active
    super_admin or when the actor is the target user.
    """
    if user_id == actor.id:
        raise CannotModifySelf("cannot modify your own role")

    user = await _load_user_with_memberships(session, user_id)

    target_role = payload.role
    target_team_id = payload.team_id

    if target_role == "super_admin":
        # Promotion to super_admin (or no-op if already super_admin).
        user.is_superuser = True
        user.updated_at = _now()
    else:
        if target_team_id is None:
            raise InvalidRoleAssignment("team_id is required when role is team_admin or developer")

        # If we are demoting *from* super_admin, make sure we are not
        # removing the last lockout-prevention seat. Take a row-level lock
        # on the entire active-super-admin set BEFORE the count check so
        # two concurrent demote requests serialize through Postgres
        # instead of both passing an optimistic ``count > 1`` check
        # (CWE-367 TOCTOU; see _lock_and_count_active_super_admins).
        if user.is_superuser:
            count = await _lock_and_count_active_super_admins(session)
            # The user being demoted is counted; demoting them takes the
            # count to (count - 1). Reject when that would hit zero.
            if user.is_active and count <= 1:
                raise LastSuperAdminProtected("cannot demote the last active super_admin")
            user.is_superuser = False

        # Upsert the membership row.
        existing = next(
            (m for m in user.memberships if m.team_id == target_team_id),
            None,
        )
        if existing is None:
            session.add(
                Membership(
                    user_id=user.id,
                    team_id=target_team_id,
                    role=target_role,
                )
            )
        else:
            existing.role = target_role
            existing.updated_at = _now()

        user.updated_at = _now()

    await session.commit()
    # Expire so the refetch in get_user_detail picks up the new membership
    # row instead of serving the User from the identity map (which may have
    # been loaded without a memberships join).
    log.info(
        "admin.user.role_changed",
        actor_id=str(actor.id),
        target_user_id=str(user_id),
        new_role=target_role,
        team_id=str(target_team_id) if target_team_id else None,
    )

    # Reload with memberships for the response.
    return await get_user_detail(session, actor=actor, user_id=user_id)


# ---------------------------------------------------------------------------
# Activate / deactivate
# ---------------------------------------------------------------------------


async def deactivate_user(
    session: AsyncSession,
    *,
    actor: CurrentUser,
    user_id: uuid.UUID,
) -> AdminUserDetail:
    """
    Set ``is_active = False`` and revoke every live refresh token.

    Safety:
      - Cannot deactivate self (cannot_modify_self=true).
      - Cannot deactivate the last active super_admin (last_super_admin_protected=true).
    """
    if user_id == actor.id:
        raise CannotModifySelf("cannot deactivate yourself")

    user = await _load_user_with_memberships(session, user_id)
    if not user.is_active:
        # Already deactivated — idempotent no-op. Still emit the detail.
        return await get_user_detail(session, actor=actor, user_id=user_id)

    if user.is_superuser:
        # Lock the active-super-admin row set inside this transaction
        # before the count check so two concurrent deactivations cannot
        # both pass the ``count > 1`` guard. CWE-367 TOCTOU fix.
        count = await _lock_and_count_active_super_admins(session)
        if count <= 1:
            raise LastSuperAdminProtected("cannot deactivate the last active super_admin")

    now = _now()
    user.is_active = False
    user.updated_at = now

    # Revoke every live refresh token so the deactivated user can no longer
    # rotate even if they hold a fresh refresh cookie. We pull live rows and
    # mutate them so the audit listener captures each revocation.
    live_tokens = (
        (
            await session.execute(
                select(RefreshToken).where(
                    RefreshToken.user_id == user_id,
                    RefreshToken.revoked_at.is_(None),
                )
            )
        )
        .scalars()
        .all()
    )
    for token in live_tokens:
        token.revoked_at = now
        token.revoked_reason = "logout"

    await session.commit()
    log.info(
        "admin.user.deactivated",
        actor_id=str(actor.id),
        target_user_id=str(user_id),
        revoked_token_count=len(live_tokens),
    )

    return await get_user_detail(session, actor=actor, user_id=user_id)


async def activate_user(
    session: AsyncSession,
    *,
    actor: CurrentUser,
    user_id: uuid.UUID,
) -> AdminUserDetail:
    """Set ``is_active = True``. Idempotent on already-active users."""
    user = await _load_user_with_memberships(session, user_id)
    if user.is_active:
        return await get_user_detail(session, actor=actor, user_id=user_id)

    user.is_active = True
    user.updated_at = _now()
    await session.commit()
    log.info(
        "admin.user.activated",
        actor_id=str(actor.id),
        target_user_id=str(user_id),
    )
    return await get_user_detail(session, actor=actor, user_id=user_id)


# ---------------------------------------------------------------------------
# Password reset
# ---------------------------------------------------------------------------


# Hard-coded TTL for now. Phase 6 PR #18 may make this env-driven once the
# email channel ships. CLAUDE.md core rule #11 forbids reading env vars at
# module import time, so when that change happens it will move to a runtime
# `os.getenv()` inside this function.
_PASSWORD_RESET_TTL = timedelta(hours=1)


async def initiate_password_reset(
    session: AsyncSession,
    *,
    actor: CurrentUser,
    user_id: uuid.UUID,
) -> uuid.UUID:
    """
    Generate a one-shot reset token, persist its bcrypt hash, return the row id.

    The plaintext token is returned to the caller (the HTTP route) only so
    Phase 6 PR #18 can plug an email sender in front of this service. The
    current router discards it after logging — see api/v1/admin/users.py.

    Returns the new ``password_reset_tokens.id``. The plaintext is NOT
    returned: this service is responsible for hashing + persisting and the
    route is responsible for the side-channel delivery.
    """
    user = await _load_user_with_memberships(session, user_id)
    now = _now()

    # Single-pending-token policy: invalidate any prior live tokens for this
    # user before issuing a new one. We pull rows and mutate so the audit
    # listener captures each invalidation.
    live = (
        (
            await session.execute(
                select(PasswordResetToken).where(
                    PasswordResetToken.user_id == user_id,
                    PasswordResetToken.used_at.is_(None),
                    PasswordResetToken.invalidated_at.is_(None),
                    PasswordResetToken.expires_at > now,
                )
            )
        )
        .scalars()
        .all()
    )
    for prior in live:
        prior.invalidated_at = now

    plaintext = secrets.token_urlsafe(32)
    token_hash = hash_password(plaintext)
    row = PasswordResetToken(
        user_id=user.id,
        token_hash=token_hash,
        expires_at=now + _PASSWORD_RESET_TTL,
    )
    session.add(row)
    await session.commit()
    await session.refresh(row)

    log.info(
        "admin.user.password_reset_initiated",
        actor_id=str(actor.id),
        target_user_id=str(user_id),
        reset_token_id=str(row.id),
        invalidated_count=len(live),
    )

    # Plaintext is intentionally never returned. The model's id is enough
    # for the route to log + emit the audit event.
    del plaintext  # explicit drop — make the discard intent obvious
    return row.id


